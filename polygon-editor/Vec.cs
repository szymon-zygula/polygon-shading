namespace polygon_editor {
    public struct Vec2 {
        public double X, Y;

        public Vec2(double x, double y) {
            X = x;
            Y = y;
        }
    }

    public struct Vec3 {
        public double X, Y, Z;

        public Vec3(double x, double y, double z) {
            X = x;
            Y = y;
            Z = z;
        }
    }
}
